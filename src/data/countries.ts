/**
 * Check if a typed word is a related name/demonym of the same country
 * as the expected word, but in the wrong form.
 * Returns 'demonym' if expected is a demonym and typed is a name,
 * 'country' if expected is a name and typed is a demonym,
 * or null if they aren't related.
 */
export function checkWrongForm(typed: string, expected: string): 'demonym' | 'country' | null {
  // Strip trailing punctuation so "Georgian," still matches "Georgian"
  const strip = (s: string) => s.toLowerCase().replace(/[^a-zA-Z\u00C0-\u024F]+$/g, '');
  const typedLower = strip(typed);
  const expectedLower = strip(expected);
  if (typedLower === expectedLower) return null;

  // Find the country that the expected word belongs to
  const expectedCountry = countries.find((c) => {
    const allTerms = [c.name, ...c.demonyms, ...c.aliases].map((t) => t.toLowerCase());
    return allTerms.includes(expectedLower);
  });
  if (!expectedCountry) return null;

  // Check if the typed word also belongs to the same country
  const allTerms = [expectedCountry.name, ...expectedCountry.demonyms, ...expectedCountry.aliases];
  const typedMatch = allTerms.find((t) => t.toLowerCase() === typedLower);
  if (!typedMatch) return null;

  // Determine what form the expected word is — return what the user SHOULD use
  const expectedIsDemonym = expectedCountry.demonyms.some((d) => d.toLowerCase() === expectedLower);

  return expectedIsDemonym ? 'demonym' : 'country';
}
